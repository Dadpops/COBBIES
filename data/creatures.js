/** XP thresholds for evolution: stage 0 default, ->1 at 120, ->2 at 320. */
export const EVO_XP = [0, 120, 320];

/** Resolve an evolution stage from a raw XP value. */
export function stageFor(xp) {
  let s = 0;
  for (let i = 0; i < EVO_XP.length; i++) if (xp >= EVO_XP[i]) s = i;
  return s;
}

