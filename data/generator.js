/* ---------- names ---------- */
const PRE = ['bo', 'mi', 'lu', 'ka', 'ni', 'po', 'ta', 'ze', 'fi', 'ru', 'da', 'go', 'he', 'ji', 'ko', 'ly', 'mo', 'ne', 'qu', 'sa', 'tu', 'vi', 'wu', 'yo'];
const SUF = ['bo', 'mi', 'ka', 'ly', 'po', 'za', 'no', 'nu', 'do', 'fo', 'gu', 'ki', 'lo', 'mu', 'pi', 'ro', 'so', 'to'];
function makeName(rng) {
  const n = PRE[(rng() * PRE.length) | 0] + SUF[(rng() * SUF.length) | 0];
  return n.charAt(0).toUpperCase() + n.slice(1).toUpperCase();
}

const FLAVORS = [
  'Found wandering near the ranch, now very much at home.',
  'Small, curious, and a little bit magic.',
  'Naps in sunbeams. Dreams of snacks.',
  'Would follow you anywhere. Has, in fact, already started.',
  'Hums a tune only it can hear.',
  'Collects shiny pebbles. Will show you if you ask nicely.',
  'A gentle soul with surprising hidden talents.',
  'Rare sighting. Even rarer friend.',
];

const RARITIES = ['common', 'common', 'common', 'rare', 'rare', 'legendary'];

function overlay(base, top) {
  const g = base.map((row) => row.slice());
  for (let y = 0; y < top.length && y < g.length; y++)
    for (let x = 0; x < top[y].length; x++)
      if (top[y][x]) g[y][x] = top[y][x];
  return g;
}

/**
 * Generate `count` creatures. Returns { critters, pals } keyed by `g<n>`.
 * Each critter has: name, type, rarity, flavor, stages[3], stageNames,
 * unlockAt (ranch capacity required), generated:true.
 */
export function generateBank(count, startCapacity = 10) {
  const critters = {};
  const pals = {};
  const usedNames = new Set();
  for (let i = 1; i <= count; i++) {
    const rng = mulberry32(0x9e3779b9 ^ (i * 2654435761));
    const tpl = TEMPLATES[(rng() * TEMPLATES.length) | 0];
    const palIdx = (rng() * PALETTES.length) | 0;
    const topper = TOPPERS[(rng() * TOPPERS.length) | 0];
    let name = makeName(rng);
    let guard = 0;
    while (usedNames.has(name) && guard++ < 20) name = makeName(rng);
    usedNames.add(name);
    const rarity = RARITIES[(rng() * RARITIES.length) | 0];
    const type = 'g' + i;
    pals[type] = PALETTES[palIdx];
    critters[type] = {
      name, type, rarity, generated: true,
      flavor: FLAVORS[(rng() * FLAVORS.length) | 0],
      stages: [tpl.baby, tpl.adult, overlay(tpl.adult, topper)],
      stageNames: ['BABY', 'GROWN', 'PRIME'],
      unlockAt: startCapacity + (i - 1), // higher id → needs a bigger ranch
    };
  }
  return { critters, pals };
}
