/**
 * PROCEDURAL CRITTER GENERATOR
 * ----------------------------
 * The content engine that scales the bank toward ~150 without hand-drawing
 * every sprite. A creature is composed from modular, hand-designed PARTS:
 *
 *   TEMPLATE (silhouette + 2 base stages)  ×  PALETTE (harmonised colours)
 *   ×  TOPPER (stage-2 flair: horns/crown/ears…)  ×  generated NAME
 *
 * Because colour dominates perception, ~6 templates × 24 palettes × 8 toppers
 * yields hundreds of distinct-looking critters that still feel hand-made.
 * Everything is deterministic from an integer id, so the bank is stable.
 *
 * Template index convention (shared with the palette slots):
 *   1 dark  2 body  3 light  4 belly  5 outline  6 mouth  7 eye  8 accent
 */

/* ---------- deterministic RNG ---------- */
function mulberry32(a) {
  return function () {
    a |= 0; a = (a + 0x6D2B79F5) | 0;
    let t = Math.imul(a ^ (a >>> 15), 1 | a);
    t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
    return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
  };
}

/* ---------- palette generation (HSL → hex, harmonised) ---------- */
function hsl(h, s, l) {
  h = ((h % 360) + 360) % 360; s /= 100; l /= 100;
  const c = (1 - Math.abs(2 * l - 1)) * s;
  const x = c * (1 - Math.abs(((h / 60) % 2) - 1));
  const m = l - c / 2;
  let r = 0, g = 0, b = 0;
  if (h < 60) [r, g, b] = [c, x, 0];
  else if (h < 120) [r, g, b] = [x, c, 0];
  else if (h < 180) [r, g, b] = [0, c, x];
  else if (h < 240) [r, g, b] = [0, x, c];
  else if (h < 300) [r, g, b] = [x, 0, c];
  else [r, g, b] = [c, 0, x];
  const hx = (v) => Math.round((v + m) * 255).toString(16).padStart(2, '0');
  return '#' + hx(r) + hx(g) + hx(b);
}

/** A palette scheme: [null, dark, body, light, belly, outline, mouth, eye, accent] */
function makePalette(hue, sat) {
  return [
    null,
    hsl(hue, sat, 34),               // 1 dark
    hsl(hue, sat, 54),               // 2 body
    hsl(hue, sat, 67),               // 3 light
    hsl(hue + 18, sat * 0.5, 88),    // 4 belly (cream)
    hsl(hue, sat * 0.55, 16),        // 5 outline
    hsl(hue + 330, 60, 62),          // 6 mouth (soft pink)
    '#20181c',                       // 7 eye
    hsl(hue + 175, Math.min(90, sat + 15), 58), // 8 accent (complementary)
  ];
}

